using System;

namespace Tugas_1
{
    class BangunDatar
    {
            public double LuasPersegi(double s)
            {
                return s * s;
            }

            public double KelilingPersegi(double s)
            {
                return 4 * s;
            }

            public double LuasLingkaran(double r)
            {
                return Math.PI * r * r;
            }

            public double KelilingLingkaran(double r)
            {
                return 2 * Math.PI * r;
            }
        
    }

}