// FOR
for (int i = 1; i <= 5; i++)
{
    Console.WriteLine("Perulangan Ke - " + i); 
}


// WHILE LOOP
int angka = 1;

while (angka <= 5)
{
    Console.WriteLine("Angka: " + angka);

    angka++;
}


// FOREACH LOOP
string[] namaBuah = { "Apel", "Pisang", "Mangga" };

foreach (string buah in namaBuah)
{
    Console.WriteLine(buah);
}